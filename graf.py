"""
Неорієнтованим графом (англ. undirected graph) називається пара G = (V, E), 
де V — множина вершин, а E ⊆ {{v, u} : v, u ∈ V} — множина ребер. 
Ребром у неорієнтованому графі називають невпорядковану пару вершин {v, u} ∈ E.
Неорієнтований граф називається простим, якщо в ньому немає кратних ребер і петель.

Матриця суміжності графа G з n вершин, пронумерованих числами від 1 до n —
це квадратна матриця A розміру n × n, у якій значення елемента G_ij дорівнює числу ребер
з i-ї вершини графа в j-ю вершину.

Матриця суміжності простого неорієнтованого графа складається з нулів і одиниць,
ця матриця симетрична щодо головної діагоналі, на якій записані нулі (G_ij = 0).

Простий неорієнтований граф задано матрицею суміжності. 
Порахуйте кількість ребер у заданому графі.
"""


# Зчитуємо кількість вершин у графі
n = int(input())

# Зчитуємо матрицю суміжності розміром n x n
# Кожен рядок — це список з 0 і 1, які позначають наявність ребер
matrix = [list(map(int, input().split())) for _ in range(n)]

# Ініціалізуємо лічильник кількості ребер
count = 0

# Перебираємо лише верхню трикутну частину матриці
# (оскільки граф неорієнтований і матриця симетрична, ми не хочемо рахувати ребро двічі)
for i in range(n):
    for j in range(i + 1, n):
        # Якщо є ребро між вершинами i та j
        if matrix[i][j] == 1:
            count += 1  # Збільшуємо лічильник

# Виводимо кількість ребер у графі
print(count)