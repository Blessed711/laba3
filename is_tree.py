"""
Формат вхідних даних:

У першому рядку вхідних даних подається ціле число N — кількість вершин у графі.
Далі в N рядках по N чисел подається матриця суміжності графа:
в i-му рядку на j-му місці стоїть 1, якщо вершини i і j з’єднані ребром,
і 0, якщо ребра між ними немає.

На головній діагоналі матриці стоять нулі.
Матриця симетрична відносно головної діагоналі.
"""


# Функція перевіряє, чи є граф деревом
def is_tree(adj):
    N = len(adj)  # Кількість вершин у графі
    visited = [False] * N  # Масив відвіданих вершин для DFS

    # Внутрішня рекурсивна функція обходу в глибину
    def dfs(v, parent):
        visited[v] = True  # Позначаємо вершину як відвідану
        for u in range(N):
            if adj[v][u] == 1:  # Є ребро між v і u
                if not visited[u]:
                    # Якщо сусід ще не відвіданий — йдемо в нього
                    if not dfs(u, v):
                        return False
                elif u != parent:
                    # Якщо сусід уже відвіданий і це не батьківська вершина — цикл!
                    return False
        return True

    # Підрахунок кількості ребер у неорієнтованому графі
    # Кожне ребро враховується двічі в матриці суміжності
    edge_count = sum(sum(row) for row in adj) // 2
    if edge_count != N - 1:
        # У дереві має бути рівно N-1 ребро
        return False

    # Перевіряємо на наявність циклів та зв'язність за допомогою DFS
    if not dfs(0, -1):
        return False

    # Перевіряємо, чи всі вершини були відвідані (зв'язність)
    if not all(visited):
        return False

    return True