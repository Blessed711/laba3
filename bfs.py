"""
Пошук у ширину (англ. breadth-first search, BFS) — один із методів обходу графа.
Нехай задано граф G = (V, E) і виділено вихідну вершину s.
Алгоритм пошуку в ширину систематично обходить усі ребра графа G для «відкриття» всіх вершин, досяжних з s,
обчислюючи при цьому відстань (мінімальну кількість ребер) від s до кожної досягнутої з s вершини.
Алгоритм працює як для орієнтованих, так і для неорієнтованих графів.

Дано орієнтований граф. Знайдіть відстань від однієї заданої вершини до іншої.
Відстань — це кількість орієнтованих ребер заданого графа, якими треба «пройти»,
щоб потрапити з однієї заданої вершини в іншу.
"""


from collections import deque

# Функція bfs — пошук у ширину для знаходження найкоротшого шляху
def bfs(matrix, start, finish):
    n = len(matrix)  # Кількість вершин у графі
    visited = [False] * n  # Масив відвіданих вершин
    dist = [-1] * n        # Масив відстаней від стартової вершини

    queue = deque()        # Ініціалізуємо чергу для BFS
    queue.append(start)    # Додаємо стартову вершину в чергу
    visited[start] = True  # Позначаємо її як відвідану
    dist[start] = 0        # Відстань до себе — 0

    while queue:
        current = queue.popleft()  # Виймаємо вершину з черги
        for neighbor in range(n):  # Перебираємо всіх можливих сусідів
            # Якщо існує ребро і сусід ще не відвіданий
            if matrix[current][neighbor] == 1 and not visited[neighbor]:
                visited[neighbor] = True
                dist[neighbor] = dist[current] + 1  # Встановлюємо відстань
                queue.append(neighbor)  # Додаємо сусіда в чергу

                # Якщо дійшли до фінішної вершини — повертаємо результат
                if neighbor == finish:
                    return dist[neighbor]

    # Якщо шлях до finish не знайдено — повертаємо -1
    return -1


# Зчитуємо кількість вершин N, стартову вершину S, кінцеву вершину F
N, S, F = map(int, input().split())

# Зчитуємо матрицю суміжності розміром N x N
matrix = [list(map(int, input().split())) for _ in range(N)]

# Переходимо до 0-індексації (Python-списки починаються з 0)
S -= 1
F -= 1

# Викликаємо BFS та виводимо результат
print(bfs(matrix, S, F))