"""
Формат вхідних даних:

У першому рядку вхідних даних записано число K — кількість графів, які треба перевірити.
Далі йде K блоків, кожен з яких описує один неорієнтований граф списком ребер.

Для кожного блоку спочатку задано числа N — кількість вершин і M — кількість ребер,
а потім йде M рядків по 2 числа — опис ребер неорієнтованого графа.
"""


def is_transitive(n, edges):
    # Створюємо порожню матрицю суміжності розміром n x n
    adj = [[0]*n for _ in range(n)]
    
    # Заповнюємо матрицю: для кожного ребра u-v ставимо 1 в обох напрямах
    for u, v in edges:
        u -= 1  # Перетворення до 0-індексації
        v -= 1
        adj[u][v] = adj[v][u] = 1

    # Перевірка транзитивності:
    # Для кожної трійки вершин (i, v, w)
    for i in range(n):
        for v in range(n):
            # Пропускаємо однакові вершини або якщо немає ребра i–v
            if i == v or adj[i][v] == 0:
                continue
            for w in range(n):
                # Пропускаємо:
                # - однакові вершини
                # - відсутність ребра v–w
                if v == w or i == w or adj[v][w] == 0:
                    continue
                # Якщо i з'єднано з v, і v з'єднано з w,
                # але i не з'єднано з w → порушена транзитивність
                if adj[i][w] == 0:
                    return False
    # Якщо перевірка пройдена для всіх трійок — граф транзитивний
    return True

# Зчитуємо кількість графів для перевірки
K = int(input())
for _ in range(K):
    # Зчитуємо кількість вершин і кількість ребер для одного графа
    n, m = map(int, input().split())

    # Зчитуємо m ребер
    edges = [tuple(map(int, input().split())) for _ in range(m)]

    # Перевіряємо граф на транзитивність і виводимо результат
    print("YES" if is_transitive(n, edges) else "NO")