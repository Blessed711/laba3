"""
Неорієнтований зважений граф задано списком ребер.
Знайдіть сумарну вагу всіх ребер, а також ребра з мінімальною та максимальною вагою.

Формат вхідних даних:
У першому рядку вхідного файлу задано числа N — кількість вершин у графі
та M — кількість ребер.

Далі йде M рядків по 3 числа в кожному — список ребер зваженого графа:
перші два числа означають номери вершин, які з’єднує це ребро,
третє число — вага ребра.
"""


# Зчитуємо два числа:
# N — кількість вершин (не використовується напряму),
# M — кількість ребер у графі
N, M = map(int, input().split())

# Зчитуємо M ребер. Кожне ребро — це трійка: (u, v, w)
# u — перша вершина, v — друга вершина, w — вага ребра
edges = [tuple(map(int, input().split())) for _ in range(M)]

# Ініціалізуємо суму ваг усіх ребер
total_weight = 0

# Ініціалізуємо максимальне та мінімальне ребро
# max_edge: (u, v, w) — поточне ребро з найбільшою вагою
# min_edge: (u, v, w) — поточне ребро з найменшою вагою
max_edge = (-1, -1, -1)
min_edge = (-1, -1, float('inf'))

# Проходимо по всіх ребрах
for u, v, w in edges:
    # Додаємо вагу ребра до загальної суми
    total_weight += w

    # Оновлюємо максимальне ребро:
    # Якщо поточна вага більша або така сама, замінюємо (беремо останнє за входом)
    if w > max_edge[2] or (w == max_edge[2]):
        max_edge = (u, v, w)

    # Оновлюємо мінімальне ребро:
    # Якщо вага менша — оновлюємо
    if w < min_edge[2]:
        min_edge = (u, v, w)

# Виводимо:
# 1. Загальну вагу всіх ребер
# 2. Ребро з максимальною вагою
# 3. Ребро з мінімальною вагою
print(total_weight)
print(*max_edge)
print(*min_edge)